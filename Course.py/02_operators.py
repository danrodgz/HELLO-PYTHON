# OPERADORES ARITMÉTICOS
print(3 + 4) # Suma
print(3 - 4) # Resta
print(3 * 4) # Multiplicación
print(10 % 4) # Residuo
print(10 // 4) # Siempre da un número entero (el inferior)
print(2 ** 3) # Exponencial

# El signo "+" igual sirve para concatenar cadenas
# No pone espacios entre cada string automáticamente
print("Hola " + "Python")
print("Hola " + str(5))
print("Hola " * 5)

my_float = 2.5 * 2
print("Hola " * int(my_float))

